    private void bRegistroEstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRegistroEstActionPerformed
        String nombreEstudiante = nomEst.getText();
        String rutEst = rutTextF.getText();
        String numMatr = matriculaTField.getText();
        String carrera = (String) carrerasList.getSelectedItem();

        try {
            FileWriter fw = new FileWriter("Estudiantes.txt", true);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(nombreEstudiante + "," + rutEst + "," + numMatr + "," + carrera);
            bw.newLine();
            bw.close();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        JOptionPane.showMessageDialog(null, "Datos guardados correctamente.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        nomEst.setText("");
        rutTextF.setText("");
        matriculaTField.setText("");
    }//GEN-LAST:event_bRegistroEstActionPerformed

