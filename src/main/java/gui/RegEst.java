    public RegEst() {
        initComponents();
        this.setLocationRelativeTo(null);
        try {
            // Abre el archivo de carreras en modo lectura
            FileReader fr = new FileReader("carreras.txt");
            BufferedReader br = new BufferedReader(fr);

            // Crea una lista para almacenar las carreras
            List<String> carreras = new ArrayList<String>();

            // Lee el archivo línea por línea y agrega cada carrera a la lista
            String linea;
            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split(",");
                String nombreCarrera = datos[0];
                carreras.add(nombreCarrera);
            }

            // Agrega las carreras al JComboBox
            for (String carrera : carreras) {
                carrerasList.addItem(carrera);
            }

            // Cierra los streams de lectura
            br.close();
            fr.close();
        } catch (IOException ex) {
            ex.printStackTrace();
        }

    }

